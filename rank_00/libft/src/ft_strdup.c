#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	int		n;

	n = ft_strlen(s) + 1;
	dup	= malloc(sizeof(char) * n);
	if (!dup)
		return (NULL);
	ft_strlcpy(dup, s, n);
	return (dup);
}

